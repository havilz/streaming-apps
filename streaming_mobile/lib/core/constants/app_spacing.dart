/// Token spacing dan sizing (dalam logical pixel / dp).
/// Gunakan nilai ini untuk padding, margin, gap, dan ukuran elemen.
abstract final class AppSpacing {
  /// 4dp — gap antar ikon & label, jarak sangat kecil
  static const double xs = 4.0;

  /// 8dp — padding dalam badge, jarak kecil
  static const double sm = 8.0;

  /// 16dp — padding horizontal halaman (standar)
  static const double md = 16.0;

  /// 24dp — jarak antar section
  static const double lg = 24.0;

  /// 32dp — padding vertikal besar
  static const double xl = 32.0;

  /// 48dp — jarak ekstra besar
  static const double xxl = 48.0;
}
